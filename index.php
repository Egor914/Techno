<h1>Немного о мире TECHNO</h1>
 Techno - чисто электронная музыка, изначально создававшаяся для танца. В ней звуки классической германской электроники сочетаются с американским "городским мировосприятием". В этой музыке выделяются "машинные звуки", создаваемые с помощью электронных драм машин (например, Roland TR-808), и повторяющиеся ритмические фигуры (Roland TB-303).


История techno начинается в Детройте в тот момент, когда музыканты обзаводятся недорогими бывшими в употреблении электронными инструментами и начинают использовать их так, как никто до них даже не догадывался. Первые техно музыканты создавали произведения, пронизанные научно-фантастическими и футуристическими темами. Techno sound создавал образы, совершенно не похожие на стареющий Детройт. Музыка и темы песен звучали так, как будто бы были из будущего. По словам пионера техно музыки Juan Atkins'а, к процессу производства музыки, которая имеет футуристичное звучание, возникло отношение как к чему-то, чего до этого вообще не делали.


В 1988 году в свет вышла компиляция под названием Techno! The New Dance Sound. Именно с нее все и началось.Техно - сугубо функционально, то есть является инструментом: оно предназначено побуждать к танцам, но танцы эти - непростые. Танцуя под техно, следует отключаться и впадать в экстаз.Техно - электронная музыка, ее развитие связано с бурным развитием сверхсовременной аппаратуры, с применением высоких технологий.


Техно - это не бум-бум-бум-бум! Техно сложно устроено. Техно обладает душой, движением. Техно обращается к чувствам.
