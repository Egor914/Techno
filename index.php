<a name="Dj.sostav"><h1>Dj состав</h1></a>

<b>Nina Kraviz</b> – одна из лучших российских диджеев, талантливая певица и продюсер. Она стала первым российским диджеем, официально выступившем на всемирно известном фестивале Sonar. Нина Кравиц играет с 2003 года и за это время с успехом отметилась в лучших музыкальных клубах столицы (Пропаганда, Икра, Mix, Город, Пластилин, Крыша Мира, Б1 Maximum) и многочисленных модных барах и кафе (Галерея, SuzyWong, Justo).
